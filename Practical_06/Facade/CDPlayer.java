class CDPlayer
 {
	
	public void on()
	 {
		System.out.println("DVD PLAYER ON");
	 }
	public void off()
	 {
		System.out.println("DVD PLAYER OFF");
	 }
	public void play()
	 {
		System.out.println("START PLAY   ");
	 }
	public void pause()
	 {
		System.out.println("PAUSE  PLAY ");
	 }
	public void insert()
	 {
		System.out.println("INSERT THE CD ");
	 }
	public void eject()
	 {
		System.out.println("EJECT THE CD ");
	 }
	
 }
